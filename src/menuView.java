    //02-04 method ini berfungsi untuk menampilkan data pada database
    public void loadData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM cekin";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()){
                Object[] o = new Object[7];
                o [0] = r.getString("id");
                o [1] = r.getString("nama");
                o [2] = r.getString("jenisKelamin");
                o [3] = r.getString("alamat");
                o [4] = r.getString("tglCekIn");
                o [5] = r.getString("tglCekOut");
                o [6] = r.getString("JenisKamar");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }
    
    
