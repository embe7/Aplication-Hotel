    //01-03 method untuk memberi nomor id otomatis (autonumber)
    private void autonumber(){
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM cekin ORDER BY id DESC";
            ResultSet r = s.executeQuery(sql);
            if(r.next()){
                    String Nokamar = r.getString("id").substring(2);
                    String KM = "" + (Integer.parseInt(Nokamar) + 1);
                    String No1 = "";
                    
                    if(KM.length()==1){
                        No1="00";
                    }else if(KM.length()==2){
                        No1="0";
                    }else if(KM.length()==3){
                        No1="";
                    }
                    
                    txtId.setText("KM" + No1 + KM);
                }else{
                    txtId.setText("KM001");
                }
                r.close();
                s.close();
        } catch (Exception e) {
            System.out.println("aoutonumber error");
        }
    }
    
